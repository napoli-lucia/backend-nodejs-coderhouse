import mongoose from "mongoose";

const collectionName = "users";

const userSchema = new mongoose.Schema({
    first_name: {
        type: String,
        required: true,
    },
    last_name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    age: {
        type: Number,
        required: true,
    },
    password: {
        type: String,
        required: true,
    },
    role: {
        type: String,
        default: 'usuario'
    }
  });
  
const usersModel = mongoose.model(collectionName, userSchema);
export default usersModel;